#A calculator App

def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

import  math 
def sqrt(num):
    return math.sqrt(num)

def cos_function(num):
    return math.cos(num)